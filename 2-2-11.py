'''
Посчитайте сумму элементов с четными номерами. Элементы нумеруются с 0. Ноль - четное число.

Sample Input:
-0.6285039281489917
2.791435598251333
4.700136811646409
2.636634832575563
3.255500052240457
1.470663454413966
-0.2128187267121433
.

Sample Output:
7.114314209025731
'''
a = input()
c = []

while a != '.':
    c.append(float(a))
    a = input()

print(sum(c[0::2]))

